package beans;

public class Customers {
	private String cuCode;
	private String cuName;
	private String cuPhone;
	private String cuClName;
	private String cuClCode;
	
	public String getCuClCode() {
		return cuClCode;
	}
	public void setCuClCode(String cuClCode) {
		this.cuClCode = cuClCode;
	}
	public String getCuCode() {
		return cuCode;
	}
	public void setCuCode(String cuCode) {
		this.cuCode = cuCode;
	}
	public String getCuName() {
		return cuName;
	}
	public void setCuName(String cuName) {
		this.cuName = cuName;
	}
	public String getCuPhone() {
		return cuPhone;
	}
	public void setCuPhone(String cuPhone) {
		this.cuPhone = cuPhone;
	}
	public String getCuClName() {
		return cuClName;
	}
	public void setCuClName(String cuClName) {
		this.cuClName = cuClName;
	}
	
}
