package beans;

public class Orders {
	String odCode;
	String odPoSrCode;
	String odPoCode;
	String odCuCode;
	int odStCode;
	String[] goCode;
	String[] goQuantity;
	
	public String getOdCode() {
		return odCode;
	}
	public void setOdCode(String odCode) {
		this.odCode = odCode;
	}
	public String getOdPoSrCode() {
		return odPoSrCode;
	}
	public void setOdPoSrCode(String odPoSrCode) {
		this.odPoSrCode = odPoSrCode;
	}
	public String getOdPoCode() {
		return odPoCode;
	}
	public void setOdPoCode(String odPoCode) {
		this.odPoCode = odPoCode;
	}
	public String getOdCuCode() {
		return odCuCode;
	}
	public void setOdCuCode(String odCuCode) {
		this.odCuCode = odCuCode;
	}
	public int getOdStCode() {
		return odStCode;
	}
	public void setOdStCode(int odStCode) {
		this.odStCode = odStCode;
	}
	public String[] getGoCode() {
		return goCode;
	}
	public void setGoCode(String[] goCode) {
		this.goCode = goCode;
	}
	public String[] getGoQuantity() {
		return goQuantity;
	}
	public void setGoQuantity(String[] goQuantity) {
		this.goQuantity = goQuantity;
	}
	
	
}
